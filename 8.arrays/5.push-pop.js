var numbers = [1,2,3]

// push - adds a value to an array at the end
numbers.push(4)
numbers.push(5)
numbers.push(6)

// unshift - adds the value to an array at the begining
numbers.unshift(0)
numbers.unshift(-1)
numbers.unshift(-2)

// pop - removes the value from an array at the end
numbers.pop()
numbers.pop()
numbers.pop()

// shift - removes the value from an array at the begining
numbers.shift()
numbers.shift()
numbers.shift()

console.log(numbers)